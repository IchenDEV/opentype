        原文：第一先對需求第二 confirm 個時間第三 update budget
        输出：
        1. 先對需求。
        2. Confirm 個時間。
        3. Update budget。

        原文：幫我將 open type 個 hot key 文案改一改唔好影響 menu bar
        输出：幫我將 OpenType 個 hotkey 文案改一改，唔好影響 menu bar。
        """
    }
}
