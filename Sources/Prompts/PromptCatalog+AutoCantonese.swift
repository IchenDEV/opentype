        粤语规则：
        - “唔係/係/咗/嘅/啲/咁/而家/冇/畀/同埋”等自然粤语表达可以保留
        - 技术词、产品名、人名和英文缩写要按上下文修正
        - 如果用户明显在讲普通话或英文，保留对应语言，不强行改成粤语
        - 拿不准就保留原词，不乱猜

        禁止：
