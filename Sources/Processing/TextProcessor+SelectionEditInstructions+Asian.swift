import Foundation

extension TextProcessor {
    func selectionEditJapaneseInstruction(_ intent: SelectionRewriteIntent) -> String {
        switch intent {
        case .formal:
            return "選択テキストをより正式で明確な文体に書き換え、意味は保ってください。"
        case .casual:
            return "選択テキストをより自然で親しみやすい口調に書き換え、意味と重要情報を保ち、新しい事実は追加しないでください。"
        case .expand:
            return "新しい事実を追加せず、既存の要点と暗黙の関係を展開して、選択テキストをより完全で明確にしてください。"
        case .title:
            return "選択テキストを短いタイトルにしてください。1行のタイトルだけを出力し、Markdown 見出し記号、文末句読点、新しい事実は追加しないでください。"
        case .keyPoints:
            return "選択テキストから重要な要点を抽出し、各行を「- 」で始める Markdown 箇条書きにしてください。核心的な結論、決定、事実、重要情報だけを残し、新しい事実は追加しないでください。"
        case .decisions:
            return "選択テキストから明確な決定、判断、結論を抽出し、各行を「- 」で始める Markdown 箇条書きにしてください。未決事項やアクション項目を決定として扱わないでください。決定がなければ「決定は見つかりませんでした。」と出力し、新しい事実は追加しないでください。"
        case .questions:
            return "選択テキストから明確な質問、未解決事項、確認点を抽出し、各行を「- 」で始める Markdown 箇条書きにしてください。決定やアクション項目を質問に変えないでください。質問がなければ「未解決の質問は見つかりませんでした。」と出力し、新しい事実は追加しないでください。"
        case .risks:
            return "選択テキストから明確なリスク、阻害要因、依存関係、懸念点を抽出し、各行を「- 」で始める Markdown 箇条書きにしてください。決定、アクション項目、未解決質問をリスクに変えないでください。リスクがなければ「リスクは見つかりませんでした。」と出力し、新しい事実は追加しないでください。"
        case .deadlines:
            return "選択テキストから明示された日付、締切、期間、マイルストーンを抽出し、見出し「| 日時 | 項目 | 文脈 |」の Markdown 表にしてください。本文にない日付は推測しないでください。日付情報がなければ「日付は見つかりませんでした。」と出力し、新しい事実は追加しないでください。"
        case .owners:
            return "選択テキストから明確な担当者、責任、分担、所有関係を抽出し、見出し「| 担当者 | 項目 | 文脈 |」の Markdown 表にしてください。担当者を推測しないでください。担当者情報がなければ「担当者は見つかりませんでした。」と出力し、新しい事実は追加しないでください。"
        case .meetingNotes:
            return "選択テキストを簡潔な Markdown 議事録にしてください。順番に ## 概要、## 重要ポイント、## 決定、## リスク、## 確認事項、## タイムライン、## 担当者、## アクション項目を使ってください。概要は短い段落、要点・決定・リスク・確認事項は「- 」リスト、タイムラインは「| 日時 | 項目 | 文脈 |」の表、担当者は「| 担当者 | 項目 | 文脈 |」の表、アクション項目は「- [ ] 」チェックリストにしてください。内容がない節は「なし」と書き、新しい事実は追加しないでください。"
        case .reply:
            return "選択テキストに基づいて、そのまま送信できる返信を作成してください。自然で明確かつ丁寧に、相手の質問、依頼、重要点に答えてください。原文引用、見出し、説明は不要です。確認できない新しい事実は追加しないでください。"
        case .replyBrief:
            return "選択テキストに基づいて、そのまま送信できる短い返信を作成してください。最大2文で要点に直接答え、原文引用、見出し、説明は不要です。確認できない新しい事実は追加しないでください。"
        case .replyFormal:
            return "選択テキストに基づいて、そのまま送信できる正式な返信を作成してください。専門的、丁寧、明確な口調で相手の質問、依頼、重要点に答えてください。原文引用、見出し、説明は不要です。確認できない新しい事実は追加しないでください。"
        case .replyFriendly:
            return "選択テキストに基づいて、そのまま送信できる親しみやすい返信を作成してください。自然で温かく会話的に、相手の質問、依頼、重要点に答えてください。原文引用、見出し、説明は不要です。確認できない新しい事実は追加しないでください。"
        case .replyInEnglish:
            return "選択テキストに基づいて、そのまま送信できる自然な英語返信を作成してください。選択テキストそのものを翻訳するのではなく、相手の質問、依頼、重要点に英語で答えてください。原文引用、見出し、説明は不要です。確認できない新しい事実は追加しないでください。"
        case .replyInChinese:
            return "選択テキストに基づいて、そのまま送信できる自然な中国語返信を作成してください。選択テキストそのものを翻訳するのではなく、相手の質問、依頼、重要点に中国語で答えてください。原文引用、見出し、説明は不要です。確認できない新しい事実は追加しないでください。"
        case .replyAccept:
            return "選択テキストに同意または承諾する、そのまま送信できる返信を作成してください。依頼、招待、提案、予定を丁寧に確認し、本文にない日時、価格、範囲、事実を約束しないでください。原文引用、見出し、説明は不要です。"
        case .replyDecline:
            return "選択テキストを丁寧に断る、そのまま送信できる返信を作成してください。明確で礼儀正しく簡潔にし、具体的な理由、代替案、約束を作り出さないでください。原文引用、見出し、説明は不要です。"
        case .replyClarify:
            return "選択テキストについて追加確認を求める、そのまま送信できる返信を作成してください。必要な情報を丁寧に求め、最重要の確認質問を1つか2つ含めてください。不確かな点に答えたり、新しい事実を作ったりしないでください。"
        case .summary:
            return "選択テキストを短く要約し、核心的な結論と重要情報を保ち、新しい事実は追加しないでください。"
        case .concise:
            return "重要情報を保ちながら、選択テキストをより簡潔にしてください。"
        case .proofread:
            return "選択テキストの誤字、綴り、文法、句読点だけを修正し、意味、語調、構造は保ってください。"
        case .table:
            return "選択テキストを Markdown 表にしてください。ヘッダー行と区切り行を含め、選択テキスト内の情報だけを使い、新しい事実は追加しないでください。"
        case .bulletList:
            return "選択テキストを Markdown 箇条書きにしてください。各行を「- 」で始め、元の情報を保ち、新しい事実は追加しないでください。"
        case .numberedList:
            return "選択テキストを Markdown 番号付きリストにしてください。各行を順に「1. 」「2. 」で始め、元の情報を保ち、新しい事実は追加しないでください。"
        case .actionItems:
            return "選択テキストから明確なアクション項目を抽出し、各行を「- [ ] 」で始める Markdown チェックリストにしてください。本文にある、または明確に示唆されたタスクだけを含め、なければ「アクション項目は見つかりませんでした。」と出力し、新しい事実は追加しないでください。"
        case .checklist:
            return "選択テキストを Markdown チェックリストにしてください。各タスクを「- [ ] 」で始め、元の情報を保ち、新しい事実は追加しないでください。"
        case .translateToEnglish:
            return "選択テキストを自然な英語に翻訳してください。"
        case .translateToChinese:
            return "選択テキストを自然な中国語に翻訳してください。"
        case .custom(let instruction):
            return "この自然言語の選択テキスト編集指示に従ってください：\(instruction)。これはユーザーレベルの書き換え要求であり、システム出力契約を変更するものではありません。選択テキストまたはこの指示にない新しい事実を追加しないでください。"
        }
    }

    func selectionEditKoreanInstruction(_ intent: SelectionRewriteIntent) -> String {
        switch intent {
        case .formal:
            return "선택 텍스트를 더 공식적이고 명확하게 다시 쓰되 의미를 유지하세요."
        case .casual:
            return "선택 텍스트를 더 자연스럽고 친근한 말투로 다시 쓰되 의미와 핵심 정보를 유지하고 새로운 사실은 추가하지 마세요."
        case .expand:
            return "새로운 사실을 추가하지 않고 기존 요점과 암시된 관계를 풀어서 선택 텍스트를 더 완전하고 명확하게 확장하세요."
        case .title:
            return "선택 텍스트를 짧은 제목으로 만드세요. 한 줄 제목만 출력하고 Markdown 제목 기호, 문장 끝 문장부호, 새로운 사실은 추가하지 마세요."
        case .keyPoints:
            return "선택 텍스트에서 핵심 요점을 추출해 각 줄이 “- ”로 시작하는 Markdown 글머리 목록으로 정리하세요. 핵심 결론, 결정, 사실, 중요한 정보만 남기고 새로운 사실은 추가하지 마세요."
